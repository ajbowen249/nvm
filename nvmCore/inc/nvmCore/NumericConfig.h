#ifndef __NUMERICCONFIG_H__
#define __NUMERICCONFIG_H__

/*
Define numeric types per-architecture/platform.
*/

namespace nvm {
    typedef  unsigned char _ui8_t;
    typedef  signed char  _i8_t;
    typedef  unsigned short _ui16_t;
    typedef  short        _i16_t;
    typedef  unsigned int _ui32_t;
    typedef  int          _i32_t;
    typedef  long         _i64_t;
    typedef  float        _f32_t;
    typedef  double       _f64_t;
}

#endif //__NUMERICCONFIG_H__
